#!/usr/bin/env python3

"""A module has an add function that returns the sum of two floats"""


def add(num1: float, num2: float) -> float:
    """A type-annotated function that calculates the sum of two numbers

    Args:
        num1 (float): The first number
        num2 (float): The second number

    Returns:
        float: The sum of the two numbers
    """

    return num1 + num2

#!/usr/bin/env python3

"""A module has a concat function that returns a concatenated string"""


def concat(str1: str, str2: str) -> str:
    """A type-annotated function that returns a concatenated string

    Args:
        str1 (str): The first string
        str2 (str): The second string

    Returns:
        str: The concatenated string
    """

    return str1 + str2
